     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if(request()->ajax()){
            return datatables()->of(
                Category::select(
                    'id',
                    'name'
                )
            )
            ->addColumn('action', 'actions.categories')
            ->rawColumns(['action'])
            ->addIndexColumn()
            ->make(true);
        }

        return view('categories.index');
    }
    
